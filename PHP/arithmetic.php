<?php

function add($a, $b) {
    echo $a + $b;
}

add(50, 100);
echo "\n";

function subtract($a, $b) {
    echo $a - $b;
}

subtract(100, 50);
echo "\n";

function multiply($a, $b) {
    echo $a * $b;
}

multiply(2, 50);
echo "\n";

function divide($a, $b) {
    echo $a / $b;
}

divide(1000, 50);
echo "\n";

